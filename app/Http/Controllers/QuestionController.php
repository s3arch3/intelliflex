     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        // evaluate isActive
        $isActive = 0;

        if ($request->input('isActive') === 'on'){
            $isActive = 1;
        }else{
            // isActive still 0
        }

        $newQuestion = [
            'question' => $request->input('questionName', 'default'),
            'explanation' => $request->input('questionExp', 'default'),
            'is_active' => $isActive,
        ];

        Question::create($newQuestion);

        return back()->with('success', 'Your question has been maded');
    }

